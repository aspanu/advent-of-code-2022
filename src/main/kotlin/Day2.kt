val myScore = mapOf("X" to 1, "Y" to 2, "Z" to 3)
val toBeat = mapOf("A" to "Y", "B" to "Z", "C" to "X")
val toDraw = mapOf("A" to "X", "B" to "Y", "C" to "Z")
val toLose = mapOf("A" to "Z", "B" to "X", "C" to "Y")
const val drawScore = 3
const val winScore = 6

fun day2Part1(input: String): Int {
    val rounds = input.split("\n").map { it.split(" ") }
    var score = 0
    for (round in rounds) {
        val gameScore = if (toBeat[round[0]] == round[1]) 6
        else if (toDraw[round[0]] == round[1]) 3
        else 0
        score += myScore[round[1]]!! + gameScore
    }
    return score
}

val winType = mapOf("X" to 0, "Y" to 3, "Z" to 6)
val mapper = mapOf("X" to toLose, "Y" to toDraw, "Z" to toBeat)

fun day2Part2(input: String): Int {
    val rounds = input.split("\n").map { it.split(" ") }
    var score = 0
    for (round in rounds) {
        val map = mapper[round[1]]!!
        val gameScore = myScore[map[round[0]]!!]!!

        score += winType[round[1]]!! + gameScore
    }
    return score
}

